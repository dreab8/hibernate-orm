import static java.lang.annotation.RetentionPolicy.RUNTIME;

/**
 * {@link org.junit.jupiter.api.AfterAll} equilvalent.
 *
 * @author Chris Cranford
 */
@Target( METHOD )
@Retention( RUNTIME )
@Inherited
public @interface DynamicAfterAll {
}
